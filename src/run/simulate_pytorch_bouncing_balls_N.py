def _plot_xy_overlay(times_a, traj_a, times_b, traj_b, dae_data, output_path):
    import matplotlib.pyplot as plt
    from matplotlib import colormaps

    state_names = [s["name"] for s in dae_data["states"]]
    n_balls = sum(1 for n in state_names if n.startswith("x") and n[1:].isdigit())
    p = {pp["name"]: pp["value"] for pp in dae_data["parameters"]}
    cmap = colormaps.get_cmap("hsv").resampled(n_balls)

    fig, ax = plt.subplots(figsize=(9, 9))
    for i in range(1, n_balls + 1):
        xi = state_names.index(f"x{i}")
        yi = state_names.index(f"y{i}")
        c = cmap(i - 1)
        ax.plot(traj_a[:, xi], traj_a[:, yi], "-", color=c, alpha=0.65,
                linewidth=1.0, label=f"IDA B{i}" if i == 1 else None)
        ax.plot(traj_b[:, xi], traj_b[:, yi], "--", color=c, alpha=0.65,
                linewidth=1.0, label=f"PyTorch B{i}" if i == 1 else None)
        ax.plot(traj_a[0, xi], traj_a[0, yi], "o", color=c, markersize=5,
                markeredgecolor="k", markeredgewidth=0.5)

    ax.plot([p["x_min"], p["x_max"], p["x_max"], p["x_min"], p["x_min"]],
            [p["y_min"], p["y_min"], p["y_max"], p["y_max"], p["y_min"]],
            "k--", alpha=0.4, linewidth=1)
    pad = 0.05 * max(p["x_max"] - p["x_min"], p["y_max"] - p["y_min"])
    ax.set_xlim(p["x_min"] - pad, p["x_max"] + pad)
    ax.set_ylim(p["y_min"] - pad, p["y_max"] + pad)
    ax.set_aspect("equal", adjustable="box")
    ax.set_xlabel("x position")
    ax.set_ylabel("y position")
    ax.set_title(
        f"{n_balls}-ball trajectories: IDA (solid) vs PyTorch (dashed)"
    )
    ax.grid(True, alpha=0.3)
    ax.legend(loc="upper right", fontsize=9)
    plt.tight_layout()
    plt.savefig(output_path, dpi=150)
    plt.close(fig)
    print(f"Plot saved to: {output_path}")


def _plot_y_timeseries_overlay(times_a, traj_a, times_b, traj_b, dae_data, output_path):
    import matplotlib.pyplot as plt
    from matplotlib import colormaps

    state_names = [s["name"] for s in dae_data["states"]]
    n_balls = sum(1 for n in state_names if n.startswith("x") and n[1:].isdigit())
    p = {pp["name"]: pp["value"] for pp in dae_data["parameters"]}
    cmap = colormaps.get_cmap("hsv").resampled(n_balls)

    fig, (ax_x, ax_y) = plt.subplots(2, 1, figsize=(14, 9), sharex=True)

    for i in range(1, n_balls + 1):
        xi = state_names.index(f"x{i}")
        yi = state_names.index(f"y{i}")
        c = cmap(i - 1)
        ax_x.plot(times_a, traj_a[:, xi], "-",  color=c, alpha=0.7, linewidth=0.9,
                  label=f"IDA B{i}" if i == 1 else None)
        ax_x.plot(times_b, traj_b[:, xi], "--", color=c, alpha=0.7, linewidth=0.9,
                  label=f"PyTorch B{i}" if i == 1 else None)
        ax_y.plot(times_a, traj_a[:, yi], "-",  color=c, alpha=0.7, linewidth=0.9)
        ax_y.plot(times_b, traj_b[:, yi], "--", color=c, alpha=0.7, linewidth=0.9)

    for ref in (p["y_min"], p["y_max"]):
        ax_y.axhline(ref, color="k", linestyle=":", alpha=0.4)
    for ref in (p["x_min"], p["x_max"]):
        ax_x.axhline(ref, color="k", linestyle=":", alpha=0.4)

    ax_x.set_ylabel("x position")
    ax_x.set_title(f"x(t) and y(t) — IDA (solid) vs PyTorch (dashed)  "
                   f"({n_balls} balls)")
    ax_x.grid(True, alpha=0.3)
    ax_x.legend(loc="upper right", fontsize=9)

    ax_y.set_xlabel("time [s]")
    ax_y.set_ylabel("y position")
    ax_y.grid(True, alpha=0.3)

    plt.tight_layout()
    plt.savefig(output_path, dpi=150)
    plt.close(fig)
    print(f"Plot saved to: {output_path}")


def main():
    config_path = os.path.join(root_dir, "config", "config_bouncing_balls_N25.yaml")
    output_dir = os.path.join(root_dir, "results")
    compare_and_plot(config_path, output_dir)


if __name__ == "__main__":
    main()
